package JavaClasses;

public class SwitchCase {
	public static void main(String[] args) {
		String dayName="tuensday";
		switch(dayName) {
		case "sunday" : System.out.println("daynumber is 1");break;
		case "monday" : System.out.println("daynumber is 2");break;
		case "tuesday" : System.out.println("daynumber is 3");break;
		case "wednesday" : System.out.println("daynumber is 4");break;
		case "thursday" : System.out.println("daynumber is 5");break;
		case "friday" : System.out.println("daynumber is 6");break;
		case "saturday" : System.out.println("daynumber is 7");break;
		default : System.out.println("dayName is wrong");
		}
	}
}
